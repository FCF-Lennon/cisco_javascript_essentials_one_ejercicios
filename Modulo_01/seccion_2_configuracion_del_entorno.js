/*
SECCIÓN 2 - CONFIGURACIÓN DEL ENTORNO DE PROGRAMACIÓN EN JAVASCRIPT

1.  Herramientas de Desarrollo:

    Para programar en JavaScript se necesita un editor de código, un intérprete 
    (como el navegador o Node.js) y un depurador. 
    Esto permite escribir, ejecutar y analizar el código.

No se requiere un equipo potente. Un computador que soporte tareas básicas es suficiente. 
El sistema operativo no es relevante.

Opciones de trabajo:

- Entorno local: instalado en el equipo, recomendado para desarrollos reales.
- Entorno en línea: útil para pruebas y aprendizaje, no requiere instalación.

2. Entornos de Desarrollo en Línea:

Son plataformas accesibles desde el navegador para escribir y ejecutar código.

Ejemplos:

- JSFiddle
- CodePen
- JsBin
- Plunker

Durante el curso se usa un entorno en línea integrado (OpenEDG) ideal para practicar.

3. Entorno de Desarrollo Local:

Componentes esenciales:

- Editor de código
- Intérprete (navegador o Node.js)
- Depurador

Herramientas adicionales para proyectos complejos:

- Gestores de paquetes (npm, yarn)
- Agrupadores de módulos (Webpack, Grunt)
- Frameworks de pruebas (Jest, Mocha)
- Analizadores de seguridad (Snyk, OWASP)

4. Editores de Código Recomendados:

- Visual Studio Code: gratuito y muy completo.
- WebStorm: comercial, ideal para proyectos grandes.
- Sublime Text: rápido, versión de prueba.
- Notepad++: ligero, funcional, para Windows.

5. Intérprete de JavaScript:

Ejecuta el código y detecta errores. Puede ser:
- Navegador web (Chrome o Firefox preferentemente)
- Node.js (para apps del lado del servidor)

Mantener el navegador actualizado es clave para compatibilidad con nuevas funciones del lenguaje.

6. Depurador:

Permite pausar y analizar el código paso a paso.

Todos los navegadores modernos lo incluyen, junto con:

- Inspector HTML
- Consola de JavaScript
- Depurador de código

Estos se activan con atajos de teclado que dependen del navegador y sistema operativo.

RECOMENDACIÓN:
Usar Visual Studio Code como editor y Google Chrome como navegador para una experiencia de 
desarrollo completa y cercana a la realidad profesional.
*/