# ==============================================
# 1.1 Introducción a JavaScript
# ==============================================

# ¿Por qué aprender JavaScript?
# JavaScript es uno de los lenguajes más usados en el mundo.
# Permite crear sitios web interactivos, apps web, móviles
# e incluso programar el lado del servidor con Node.js.

# ¿Qué es programar?
# Programar es dar instrucciones claras a una computadora.
# Usamos lenguajes como JavaScript para comunicar nuestras ideas.

# Lenguaje interpretado:
# Significa que el código se ejecuta línea por línea,
# sin necesidad de convertir todo el programa antes de ejecutarlo.
# (A diferencia de los lenguajes compilados, que deben transformarse
# completamente en lenguaje máquina antes de poder ejecutarse).

# Lenguaje compilado:
# Es aquel que requiere traducir todo el código fuente
# a lenguaje máquina antes de ejecutarse. Ej: C o Java.

# JavaScript: lenguaje interpretado y flexible
# Se ejecuta en el navegador o en Node.js.
# Es un lenguaje de alto nivel.

# Lenguaje de alto nivel:
# Significa que su sintaxis se parece más al lenguaje humano
# y oculta detalles complejos del hardware (como memoria o CPU).

# Ventajas:
# - Comunidad activa (mucha gente lo usa y ayuda en foros)
# - Fácil de aprender
# - Muchos recursos gratuitos
# - Frameworks poderosos como React y Angular

# Framework:
# Conjunto de herramientas y estructuras predefinidas
# que facilitan el desarrollo de aplicaciones.

# Desventajas:
# - No es ideal para cálculos matemáticos muy complejos
# - No ofrece el mejor rendimiento para tareas pesadas
# - El código se puede ver en el navegador y copiar fácilmente

# Cliente vs Servidor:
# - Lado del cliente: el código se ejecuta en el navegador del usuario.
# - Lado del servidor: el código se ejecuta en un servidor, como Node.js.

# Conclusión:
# JavaScript no es perfecto, pero sí excelente para comenzar a programar.
# Aprenderlo facilita aprender otros lenguajes en el futuro.

# ==============================================
# 1.2 ¡Prepárate para escribir código!
# ==============================================

# Este curso te enseñará lo esencial de JavaScript:
# - Variables: para guardar datos
# - Funciones: para agrupar instrucciones que se pueden reutilizar
# - Condicionales: para tomar decisiones (if, else)
# - Bucles: para repetir acciones (for, while)

# Estos conceptos se aplican tanto en el navegador
# como en otros entornos como Node.js.

# Consejo:
# Aprender a programar toma tiempo y práctica.
# No te rindas si te sientes perdido al principio.
# La clave es escribir mucho código y ser constante.